package com.example.activity;

public class Card {

    int id;
    String adrian;
    String guta;
    String salam;
    String avalcea;
    String jador;
    String vijelie;

    public Card(int id, String adrian, String guta, String salam, String avalcea, String jador, String vijelie) {
        this.id = id;
        this.adrian = adrian;
        this.guta = guta;
        this.salam = salam;
        this.avalcea = avalcea;
        this.jador = jador;
        this.vijelie = vijelie;
    }
}
